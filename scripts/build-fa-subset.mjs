// Generates a self-hosted, subsetted Font Awesome (6.5.1) containing ONLY the
// icons this site actually uses — replacing the render-blocking full CDN kit.
//
// It scans the source for `fas|far|fab fa-name` classes, looks up each glyph's
// codepoint in FA's own all.css (so aliases like fa-home -> \f015 resolve), then
// subsets each woff2 to just those glyphs and writes a tiny CSS + fonts into
// public/fa/. Run with `npm run fa:subset` whenever icon usage changes.
import { mkdirSync, readFileSync, readdirSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import subsetFont from 'subset-font'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const faDir = resolve(root, 'node_modules/@fortawesome/fontawesome-free')
const outDir = resolve(root, 'public/fa')
const outFonts = resolve(outDir, 'webfonts')
mkdirSync(outFonts, { recursive: true })

const STYLE_OF = { fas: 'solid', far: 'regular', fab: 'brands' }
const FONT_FILE = {
  solid: 'fa-solid-900.woff2',
  regular: 'fa-regular-400.woff2',
  brands: 'fa-brands-400.woff2',
}
const FACE = {
  'fa-solid-900.woff2': { family: 'Font Awesome 6 Free', weight: 900 },
  'fa-regular-400.woff2': { family: 'Font Awesome 6 Free', weight: 400 },
  'fa-brands-400.woff2': { family: 'Font Awesome 6 Brands', weight: 400 },
}

// 1. Collect used "<prefix> fa-<name>" pairs from source.
const files = []
const walk = (dir) => {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const p = resolve(dir, entry.name)
    if (entry.isDirectory()) walk(p)
    else if (/\.(jsx?|html)$/.test(entry.name)) files.push(p)
  }
}
walk(resolve(root, 'src'))
files.push(resolve(root, 'index.html'))

const used = { solid: new Set(), regular: new Set(), brands: new Set() }
const usageRe = /\b(fas|far|fab) fa-([a-z0-9-]+)/g
for (const file of files) {
  const text = readFileSync(file, 'utf8')
  let m
  while ((m = usageRe.exec(text))) used[STYLE_OF[m[1]]].add(m[2])
}

// 2. Build name -> codepoint map from FA's own all.css (handles aliases).
const css = readFileSync(resolve(faDir, 'css/all.css'), 'utf8')
const nameToHex = new Map()
// Solid/regular rules use ::before, brands use :before — accept either.
const ruleRe = /\.fa-([a-z0-9-]+):{1,2}before\s*\{\s*content:\s*"\\([0-9a-f]+)"/g
let r
while ((r = ruleRe.exec(css))) if (!nameToHex.has(r[1])) nameToHex.set(r[1], r[2])

// 3. Resolve glyphs per font file; fail loudly on any unknown icon.
const missing = []
const charsByFile = {}
const iconRules = []
for (const [style, names] of Object.entries(used)) {
  for (const name of names) {
    const hex = nameToHex.get(name)
    if (!hex) {
      missing.push(`${style}:fa-${name}`)
      continue
    }
    const file = FONT_FILE[style]
    ;(charsByFile[file] ||= new Set()).add(String.fromCodePoint(parseInt(hex, 16)))
    iconRules.push(`.fa-${name}::before{content:"\\${hex}"}`)
  }
}
if (missing.length) throw new Error(`Unknown icons (not in FA all.css): ${missing.join(', ')}`)

// 4. Subset each used font file to just its glyphs.
const fontFaces = []
for (const [file, chars] of Object.entries(charsByFile)) {
  const original = readFileSync(resolve(faDir, 'webfonts', file))
  const subset = await subsetFont(original, Array.from(chars).join(''), { targetFormat: 'woff2' })
  writeFileSync(resolve(outFonts, file), subset)
  const { family, weight } = FACE[file]
  fontFaces.push(
    `@font-face{font-family:"${family}";font-style:normal;font-weight:${weight};font-display:block;` +
      `src:url("./webfonts/${file}") format("woff2")}`,
  )
  console.log(`${file}: ${chars.size} glyphs, ${(subset.length / 1024).toFixed(1)}KB (was ${(original.length / 1024).toFixed(1)}KB)`)
}

// 5. Emit the CSS (font-faces + base classes + spin + per-icon content rules).
const base = [
  '.fa,.fas,.far,.fab{-webkit-font-smoothing:antialiased;display:inline-block;font-style:normal;font-variant:normal;line-height:1;text-rendering:auto}',
  '.fas{font-family:"Font Awesome 6 Free";font-weight:900}',
  '.far{font-family:"Font Awesome 6 Free";font-weight:400}',
  '.fab{font-family:"Font Awesome 6 Brands";font-weight:400}',
  '.fa-spin{animation:fa-spin 2s linear infinite}',
  '@keyframes fa-spin{from{transform:rotate(0)}to{transform:rotate(360deg)}}',
  '@media (prefers-reduced-motion:reduce){.fa-spin{animation:none}}',
].join('\n')

const out =
  '/* Auto-generated by scripts/build-fa-subset.mjs (Font Awesome 6.5.1 subset). Run `npm run fa:subset` to regenerate. */\n' +
  fontFaces.join('\n') +
  '\n' +
  base +
  '\n' +
  iconRules.sort().join('\n') +
  '\n'

writeFileSync(resolve(outDir, 'fontawesome-subset.css'), out)
console.log(`\nWrote public/fa/fontawesome-subset.css — ${iconRules.length} icon rules.`)
console.log('Used per style:', Object.fromEntries(Object.entries(used).map(([k, v]) => [k, v.size])))
